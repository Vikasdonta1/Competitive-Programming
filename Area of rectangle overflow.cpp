/* Given rectangle of length l, b. Print area

constraints (range of numbers)
1 <= l <=10^9
1 <= b <=10^9

Input format:
2 space seperated int l and b

Output format:
Single number which is area of rectangle

Sample in:
3 4;
Sam Out;
12
*/
#include<bits/stdc++.h>
using namespace std;

int main(){
	int l,b;
	cin>> l >>b;
	cout<< 1LL* l * b;
}